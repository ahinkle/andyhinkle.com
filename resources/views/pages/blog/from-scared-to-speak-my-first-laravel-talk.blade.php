    <h1 class="text-center font-sans text-3xl font-bold text-white md:text-5xl">From Scared to Speak: My First Laravel Talk</h1>

    <div class="mx-auto mt-8 max-w-3xl">
        <p class="text-balance text-left text-lg text-white/85 md:text-wrap">
            Five Laracons. That's how many I'd attended before I finally worked up the nerve to give my first tech talk. And honestly? I was terrified.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            At my very first Laracon, I knew people from Twitter (I'll forever call it that), but I was scared to death to walk up and actually say hello. 
            There's something weird about seeing someone you've interacted with online but never spoken to in person. 
            What I learned, though, is that the Laravel community is different. It's welcoming in a way that makes those awkward introductions feel natural. 
            Nobody thinks it's weird when you walk up with a handshake and just start talking.
        </p>

        <img src="/images/blog/meeting-taylor-otwell.jpg" alt="My first Laracon, Meeting Taylor Otwell"
            class="mx-auto mt-8 max-w-[400px] rounded-lg px-4 py-2">

        <h2 class="mt-8 text-balance text-left text-xl font-bold text-white md:text-wrap md:text-2xl">
            The Spark That Started Everything
        </h2>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            Everything changed after <a href="https://www.youtube.com/watch?v=2YaEtaXYVtI" class="text-blue-500 hover:underline">Aaron Francis's talk at Laracon Nashville, "Publishing Your Work"</a>. 
            His message was simple: you can just do things. 
            You can just put yourself out there. I'd heard variations of this advice for years, but something about hearing it in that context, 
            surrounded by people who were actually doing it, lit a fire in me.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            That talk pushed me and a friend to <a href="https://themidwestartisanpodcast.com/" class="text-blue-500 hover:underline">start our own podcast</a>. We don't care if anyone tunes in, we mostly do it to catch up with each other. 
            But it got me comfortable with putting my voice out there, with being a little more public about my work.
        </p>

        <h2 class="mt-8 text-balance text-left text-xl font-bold text-white md:text-wrap md:text-2xl">
            The Imposter Syndrome Trap
        </h2>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
           For the longest time, I didn't share much of my work publicly. 
           There was always this voice in my head saying someone else had already done it <span class="italic">better</span>, someone already knew what I knew, so why bother? 
           Every developer deals with this imposter syndrome, but it kept me quiet for years.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            I'd listen to talk after talk and think, <span class="italic">"Maybe one day I could give a talk."</span> Then immediately: <span class="italic">"That's a dumb idea. There are tons of people better at this than me."</span>
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            Here's what I wish someone had told me earlier:
        </p>
        
        <h2 class="mt-8 text-balance text-left text-xl font-bold text-white md:text-2xl">
            Giving a talk doesn't require elite status or being the all-knowing authority on a subject. You just need to be passionate about something and willing to share what you've learned.
        </h2>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            What I'm ridiculously obsessive about is clean code and clean architecture. When code reads like a book, when things are easy and maintainable — that stuff gets me excited in ways that probably aren't normal.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            At Laracon Denver, I was talking with several people about this obsession. Margaret from the <a href="https://vehikl.com/" class="text-blue-500 hover:underline">Vehikl team</a> asked if I'd be interested in giving a talk at <a href="https://meetup.laravel.com/" class="text-blue-500 hover:underline">Laravel Worldwide</a> and said I should seriously consider it. Dan (also from Vehikl) and I discussed some potential ideas, including this clean, elegant Laravel approach I wanted to explore. He encouraged me to submit something.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            But then the imposter syndrome kicked in hard. I sat on it, wrestling with that familiar voice telling me someone else could give this talk 
            better than me. Everything had already been talked about, right? Even as I thought through my ideas, 
            I kept thinking someone more qualified should be up there instead. I'd still want to tune in and listen because it would be good content,
            but why should it be me giving it? I would love to hear this talk from someone more qualified who knows every detail about clean architecture.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            Then, a week later, I got the message: "Hey, we have an opening if you're interested. We would love to have you! It's in two weeks - we completely understand if that's too soon."
        </p>

        <h2 class="mt-8 text-balance text-left text-xl font-bold text-white md:text-wrap md:text-2xl">
            The Two-Week Sprint
        </h2>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            I'm the kind of person who sets ridiculous goals and then can't say no to them, even when they terrify me. So I said yes, and immediately, I felt that countdown clock start ticking in the corner of my vision—like those 48-hour murder mystery shows.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            Those two weeks were insanely intense. I'd work my day job, spend evenings being a dad with my wife and son, then from 9 PM until 1 or 2 AM, I'd work on the talk. Slides, research, content.. clean code is a massive topic, so narrowing it down was its own challenge.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            Not an hour passed without me thinking about this talk. It consumed my thoughts completely. The stress was unlike anything I'd experienced before - anxiety levels I didn't know I was capable of reaching.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            The day before the talk, I woke up ready to practice. I was three slides in when I noticed the animations were wrong.
            Next slide: completely out of order. I felt my stomach spinning, like I might actually throw up. For an introvert like me, 
            the thought of going into this unprepared and looking like an idiot in front of the Laravel community was devastating.
        </p>

        <h2 class="mt-8 text-balance text-left text-xl font-bold text-white md:text-wrap md:text-2xl">
            Practice, Practice, Practice
        </h2>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
          By the end of that rough day, Jacob Bennett and Dalton McCreery reached out offering to listen to practice runs. This was the best thing that happened during the entire process.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            I gave the talk twice—once to each of them - their feedback was so valuable. They helped me refine the rough edges and gave me confidence that the content was solid.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            I went as far as recording myself, watching it back, and making adjustments. I wrote down everything word-for-word. I used <a href="https://superwhisper.com" class="text-blue-500 hover:underline">SuperWhisper</a> to record myself going through the slides, then copied and pasted exactly what I said.
            This way, if my frenetic energy took over and I lost my train of thought, I had a safety net to get back on track.
        </p>

        <p class="mt-4 text-balance text-left text-lg text-white/85 md:text-wrap">
            I started with bullet points, but even before the presentation, I was getting nervous about losing my train of thought and going completely off the rails. When you have frenetic energy like mine, conversations can go anywhere. Having everything deliberately planned out, then practicing it over and over until it didn't sound robotic, made all the difference.
        </p>

